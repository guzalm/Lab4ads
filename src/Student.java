public class Student {
    private String name;
    private int id;
    // other fields and methods

    public Student(String name, int id) {
        this.name = name;
        this.id = id;
    }

    // getters and setters for name and id
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}

